# Operators
#  Arthimetic operator[+,-,*,/,%]
#  Relational operator[<,<=,==,!=,=>,>]
#  Bit-wise operator[&,|,^,<<,>>]

a=5
b=4

#  Bit-wise operator[&,|,^,<<,>>]
and_c=a&b
print(and_c)

or_c=a|b
print(or_c)

xor_c=a^b
print(xor_c)

leftshif=a<<1
print(leftshif)

rightshift=a>>1
print(rightshift)

